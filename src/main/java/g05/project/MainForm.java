    private void searchEmployee() {
        
        String id = textTest.getText().trim();
        
        String query = "SELECT * FROM `employee_list` WHERE srno = ? AND remove=1";
        ArrayList list = new ArrayList();
        list.add(id);
        
        try {
            ResultSet search = Myconnection.search(query, list);
            
            if (search.next()) {
                isEmployeeExist = true;
                txtFname1.setText(search.getString("fname"));
                txtLname1.setText(search.getString("lname"));
                txtdepartment1.setText(search.getString("department"));
                txtdesignation1.setText(search.getString("designation"));
                txtNumber1.setText(search.getString("number"));
                if (search.getString("status").equals("Active")) {
                    r_inactive1.setSelected(false);
                    r_active1.setSelected(true);
                } else {
                    r_active1.setSelected(false);
                    r_inactive1.setSelected(true);
                }
                txtAddress1.setText(search.getString("address"));
            } else {
                isEmployeeExist = false;
                JOptionPane.showMessageDialog(null, "Invalid ID");
                clearFields();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
